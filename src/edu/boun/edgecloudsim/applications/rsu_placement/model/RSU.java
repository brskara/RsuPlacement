package edu.boun.edgecloudsim.applications.rsu_placement.model;

import org.cloudbus.cloudsim.Host;
import org.cloudbus.cloudsim.Pe;
import org.cloudbus.cloudsim.VmScheduler;
import org.cloudbus.cloudsim.provisioners.BwProvisioner;
import org.cloudbus.cloudsim.provisioners.RamProvisioner;

import java.util.List;

public class RSU extends Host {
	private Coordinate coordinate;

	public RSU(int id, RamProvisioner ramProvisioner,
			   BwProvisioner bwProvisioner, long storage,
			   List<? extends Pe> peList, VmScheduler vmScheduler, Coordinate coordinate) {
		super(id, ramProvisioner, bwProvisioner, storage, peList, vmScheduler);
		this.coordinate = coordinate;
	}

	public Coordinate getCoordinate() {
		return coordinate;
	}

	public void setCoordinate(Coordinate coordinate) {
		this.coordinate = coordinate;
	}
}
